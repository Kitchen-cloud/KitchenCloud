namespace KitchenCloudEntities.Common
{
   public class Country
    {
        private int id;

        public int Id
        {
            get { return id; }
            set { id = value; }
        }


        private int code;

        public int Code
        {
            get { return code; }
            set { code = value; }
        }


        private string name;

        public string Name
        {
            get { return name; }
            set { name = value; }
        }







    }
}
